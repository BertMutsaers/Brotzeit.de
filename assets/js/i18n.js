const LANG_KEY = 'brotzeit_lang';
const SUPPORTED = ['de', 'en', 'fr', 'nl', 'pl'];

function detectLang() {
  const stored = localStorage.getItem(LANG_KEY);
  if (stored && SUPPORTED.includes(stored)) return stored;
  const browser = (navigator.language || 'de').slice(0, 2).toLowerCase();
  return SUPPORTED.includes(browser) ? browser : 'de';
}

function applyLang(lang) {
  const t = translations[lang] || translations.de;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  // Update dropdown trigger label
  const trigger = document.querySelector('.lang-trigger');
  const activeBtn = document.querySelector(`.lang-menu .lang-btn[data-lang="${lang}"]`);
  if (trigger && activeBtn) {
    trigger.querySelector('.lang-flag').textContent = activeBtn.dataset.flag;
    trigger.querySelector('.lang-code').textContent = lang.toUpperCase();
  }
  // Close dropdown
  document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
  localStorage.setItem(LANG_KEY, lang);
}

function initI18n() {
  const lang = detectLang();
  applyLang(lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });
}

document.addEventListener('DOMContentLoaded', initI18n);
